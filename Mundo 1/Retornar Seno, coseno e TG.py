import math
ang = float(input('Digite o 창ngulo: '))
seno = math.sin(math.radians(ang))
print('O 창ngulo de {} tem SENO de {:.2f}' .format(ang,seno))
cos = math.cos(math.radians(ang))
print('O 창ngulo de {} tem CONSSENO de {:.2f} ' .format(ang,cos))
tan = math.tan(math.radians(ang))
print('O 창ngulo de {} tem TANGENTE de {:.2f}' .format(ang,tan))